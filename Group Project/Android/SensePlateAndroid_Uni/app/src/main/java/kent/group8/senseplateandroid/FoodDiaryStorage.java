package kent.group8.senseplateandroid;

class FoodDiaryStorage {
    String id, foodID, food, calories, mealType, date, time;

    FoodDiaryStorage(String id, String foodID, String food, String calories, String mealType, String date, String time) {
        this.id = id;
        this.foodID = foodID;
        this.food = food;
        this.calories = calories;
        this.mealType = mealType;
        this.date = date;
        this.time = time;
    }
}
